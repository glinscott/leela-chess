uint64_t reverse(uint64_t x) {
  uint64_t r;
  auto a = reinterpret_cast<char*>(&x);
  auto b = reinterpret_cast<char*>(&r);

  for (int i = 0; i < 4; i++) {
    b[i] = a[7-i];
    b[7-i] = a[i];
  }

  return r;
}


